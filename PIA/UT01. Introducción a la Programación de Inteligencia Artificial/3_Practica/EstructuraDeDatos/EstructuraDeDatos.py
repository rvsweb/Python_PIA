# Crear una lista básica

lista = []
print(type(lista))
lista = [1, 2, 4, 5, 6]
print(lista)
print("Elemento Basico: ", lista[1])

# Crear una tupla básica

tupla = ()
print(type(tupla))

tupla = (1, 2, 3)
print(tupla)
print(tupla[1])
print("Elemento Basico: ", tupla[1])

# Crear un diccionario básico
diccionario = {}
diccionario = {'uno': 1, 'two': 2, 'three': 3}
print(type(diccionario))
print(diccionario)
print("Valor de un elemento: ", diccionario['uno'])
